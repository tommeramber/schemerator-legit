HTTP_BODY_STRING_REGEX = [
    r'^([a-z0-9_\.-]+)@([\da-z\.-]+)\.([a-z\.]{2,6})$',  # email
    r'^(https?:\/\/)?([\da-z\.-]+)\.([a-z\.]{2,6})([\/\w \.-]*)*\/?$',  # http url
    r'^(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)$',  # ip address
    r'^([0-9a-fA-F]){8}-([0-9a-fA-F]){4}-([0-9a-fA-F]){4}-([0-9a-fA-F]){4}-([0-9a-fA-F]){12}$',  # uuid
    r'^([[:word:]]{3}), ([[:digit:]]{2}) ([[:word:]]{3}) ([[:digit:]]{4}) (([[:digit:]]{2}):([[:digit:]]{2}):([[:digit:]]{2})) GMT$', # date
    r'^(True|False|true|false)$', # boolean
    r'^(\+|-)?(?:90(?:(?:\.0{1,6})?)|(?:[0-9]|[1-8][0-9])(?:(?:\.[0-9]{1,6})?))$',  # Latitude
    r'^(\+|-)?(?:180(?:(?:\.0{1,6})?)|(?:[0-9]|[1-9][0-9]|1[0-7][0-9])(?:(?:\.[0-9]{1,6})?))$',  # Longitude
    r'^[a-zA-Z0-9_-]{3,16}$',  # username
    r'^[a-zA-Z0-9_-]{6,18}$',  # password
    r'^#?([a-f0-9]{6}|[a-f0-9]{3})$',  # hex value
    r'^[a-z0-9-]+$'  # slug
]